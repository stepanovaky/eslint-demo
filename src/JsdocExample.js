/**
 * AnyFunction takes in two numbers, adds them, then returns them in a list with a string
 * @param {number} a
 * @param {number} b
 * @param {string} c
 * @returns {String[] | Number[]}
 */

const AnyFunction = (a, b, c) => {
	const sum = a + b;

	returns[(sum, c)];
};

export default AnyFunction;
